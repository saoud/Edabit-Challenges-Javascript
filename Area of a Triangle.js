//Write a function that takes the base and height of a triangle and return its area.
//https://edabit.com/challenge/3CaszbdZYGN4otQD8

// Examples
// triArea(3, 2) ➞ 3

// triArea(7, 4) ➞ 14

// triArea(10, 10) ➞ 50
// Notes
// The area of a triangle is: (base * height) / 2


function triArea(base, height) {
	area = (base * height) / 2 
	return area
}